import static java.lang.System.*;

public class OddOrEven
{
	public static boolean isOdd( int x)
	{
		if(x %2 == 1) {
			return true;
		}
		return false;
	}
	public static boolean isEven(int y )
	{
		if(y %2 == 0) {
			return true;
		}
		return false;
	}	
}